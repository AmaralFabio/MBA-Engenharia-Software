# tipo float
print (type(1.0));

# tipo str
print (type('1'));

# tipo bool
print (type(True));

# tipo list
print (type([1, 2, 3]));

# tipo dict
print (type({'a': 1, 'b': 2}));

# tipo tuple
print (type((1, 2, 3)));

# tipo set
print (type({1, 2, 3}));

